    public async Task<string> FormatErrorMessageAsync(string error)
    {
        await Task.CompletedTask;
        
        return $"‚ùå **Error:** {error}\n\nüí° Try asking for \"help\" to see available commands, or \"status\" to check if the catalog data is loaded.";
    }

    private static string GetRingDisplayName(string audienceGroup)
    {
        return audienceGroup switch
        {
            "general" => "R4 (General)",
            "ring0" => "R0 (Ring0)",
            "ring1" => "R1 (Ring1)",
            "ring1_5" => "R1.5 (Ring1.5)",
            "ring1_6" => "R1.6 (Ring1.6)",
            "ring2" => "R2 (Ring2)",
            "ring3" => "R3 (Ring3)",
            "ring3_6" => "R3.6 (Ring3.6)",
            "ring3_9" => "R3.9 (Ring3.9)",
            _ => audienceGroup
        };
    }

    private static string FormatEntitlementState(string state)
    {
        return state switch
        {
            "InstalledAndPermanent" => "üîí Permanent",
            "Installed" => "üü¢ Installed",
            "PreConsented" => "‚úÖ Pre-consented",
            "Featured" => "‚≠ê Featured",
            "NotInstalled" => "‚ùå Not Installed",
            "InstalledAndDeprecated" => "‚ö†Ô∏è Deprecated",
            "HiddenFromAppStore" => "üö´ Hidden",
            _ => state
        };
    }
}

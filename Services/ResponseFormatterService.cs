using CatalogExpertBot.Models;
using System.Text;

namespace CatalogExpertBot.Services;

public interface IResponseFormatterService
{
    Task<string> FormatSearchResultsAsync(SearchResult searchResult);
    Task<string> FormatAppDetailsAsync(AppDetailResult appDetail);
    Task<string> FormatLoadingStatusAsync(LoadingStatus status);
    Task<string> FormatHelpMessageAsync();
    Task<string> FormatErrorMessageAsync(string error);
}

public class ResponseFormatterService : IResponseFormatterService
{
    public async Task<string> FormatSearchResultsAsync(SearchResult searchResult)
    {
        await Task.CompletedTask; // Allow for async pattern
        
        if (!searchResult.Apps.Any())
        {
            return $"üîç No apps found matching \"{searchResult.Query}\". Try a different search term or ask for help to see available commands.";
        }

        var response = new StringBuilder();
        response.AppendLine($"üîç **Found {searchResult.TotalCount} apps matching \"{searchResult.Query}\":**");
        response.AppendLine();

        for (int i = 0; i < searchResult.Apps.Count; i++)
        {
            var app = searchResult.Apps[i];
            var audienceGroupsText = string.Join(", ", app.AudienceGroups.Take(3));
            if (app.AudienceGroups.Count > 3)
            {
                audienceGroupsText += $" + {app.AudienceGroups.Count - 3} more";
            }

            var entitlementCount = app.Entitlements.Count;
            var coreAppIndicator = app.IsCoreApp ? " üè¢" : "";
            var teamsOwnedIndicator = app.IsTeamsOwned ? " ‚ö°" : "";

            response.AppendLine($"{i + 1}. üì± **{app.Name}**{coreAppIndicator}{teamsOwnedIndicator}");
            response.AppendLine($"   üè¢ {app.DeveloperName}");
            response.AppendLine($"   üìã {app.Id}");
            response.AppendLine($"   üéØ Available in: {audienceGroupsText}");
            
            if (entitlementCount > 0)
            {
                response.AppendLine($"   ‚úÖ {entitlementCount} entitlements");
            }
            
            if (!string.IsNullOrEmpty(app.ShortDescription) && app.ShortDescription.Length > 0)
            {
                var truncatedDesc = app.ShortDescription.Length > 100 
                    ? app.ShortDescription.Substring(0, 100) + "..."
                    : app.ShortDescription;
                response.AppendLine($"   üìÑ {truncatedDesc}");
            }
            
            response.AppendLine();
        }

        if (searchResult.HasMore)
        {
            response.AppendLine($"üí° **Showing {searchResult.Apps.Count} of {searchResult.TotalCount} results.** Ask for more details about a specific app by saying \"Tell me about app [App Name or ID]\"");
        }

        response.AppendLine();
        response.AppendLine("üí¨ **What would you like to know?**");
        response.AppendLine("‚Ä¢ \"Tell me about [App Name]\" - Get detailed information");
        response.AppendLine("‚Ä¢ \"Find Microsoft apps\" - Search by developer");
        response.AppendLine("‚Ä¢ \"Apps available in R1\" - Filter by audience group");
        response.AppendLine("‚Ä¢ \"Show pre-consented apps\" - Filter by entitlement state");

        return response.ToString();
    }

    public async Task<string> FormatAppDetailsAsync(AppDetailResult appDetail)
    {
        await Task.CompletedTask;
        
        var app = appDetail.AppDefinition!;
        var response = new StringBuilder();

        // Header with app info
        var coreAppIndicator = app.IsCoreApp ? " üè¢" : "";
        var teamsOwnedIndicator = app.IsTeamsOwned ? " ‚ö°" : "";
        
        response.AppendLine($"üì± **{app.Name}**{coreAppIndicator}{teamsOwnedIndicator}");
        response.AppendLine($"üè¢ **Developer:** {app.DeveloperName}");
        response.AppendLine($"üìã **App ID:** `{app.Id}`");
        response.AppendLine($"üéØ **Version:** {app.Version}");
        response.AppendLine();

        // Description
        if (!string.IsNullOrEmpty(app.ShortDescription))
        {
            response.AppendLine($"üìÑ **Description:** {app.ShortDescription}");
            response.AppendLine();
        }

        // Audience group versions
        if (appDetail.AudienceGroupVersions.Count > 1)
        {
            response.AppendLine("üåê **Audience Group Versions:**");
            foreach (var (audienceGroup, appVersion) in appDetail.AudienceGroupVersions.OrderBy(kv => kv.Key))
            {
                var ringName = GetRingDisplayName(audienceGroup);
                response.AppendLine($"‚Ä¢ {ringName}: v{appVersion.Version}");
            }
            response.AppendLine();
        }

        // Entitlements
        if (appDetail.Entitlements.Any())
        {
            response.AppendLine("üîê **Entitlement States:**");
            var groupedEntitlements = appDetail.Entitlements
                .GroupBy(e => e.AudienceGroup)
                .OrderBy(g => g.Key);

            foreach (var group in groupedEntitlements)
            {
                var ringName = GetRingDisplayName(group.Key);
                var states = group.Select(e => FormatEntitlementState(e.State)).Distinct();
                response.AppendLine($"‚Ä¢ {ringName}: {string.Join(", ", states)}");
            }
            response.AppendLine();
        }

        // Categories and capabilities
        if (app.Categories.Any())
        {
            response.AppendLine($"üè∑Ô∏è **Categories:** {string.Join(", ", app.Categories)}");
        }

        var capabilities = new List<string>();
        if (app.Bots.Any()) capabilities.Add("Bot");
        if (app.StaticTabs.Any()) capabilities.Add("Tab");
        if (app.Connectors.Any()) capabilities.Add("Connector");
        if (app.MeetingExtensionDefinition != null) capabilities.Add("Meeting Extension");
        if (app.CopilotGpts.Any()) capabilities.Add("Copilot GPT");

        if (capabilities.Any())
        {
            response.AppendLine($"‚öôÔ∏è **Capabilities:** {string.Join(", ", capabilities)}");
        }

        // Feature flags
        var features = new List<string>();
        if (app.IsCoreApp) features.Add("Core App");
        if (app.IsTeamsOwned) features.Add("Teams Owned");
        if (app.IsFullTrust) features.Add("Full Trust");
        if (app.CopilotEnabled) features.Add("Copilot Enabled");

        if (features.Any())
        {
            response.AppendLine($"üèÜ **Features:** {string.Join(", ", features)}");
        }

        response.AppendLine();
        response.AppendLine("üí¨ **Need more info?** Ask me about other apps or search for specific features!");

        return response.ToString();
    }

    public async Task<string> FormatLoadingStatusAsync(LoadingStatus status)
    {
        await Task.CompletedTask;
        
        var response = new StringBuilder();
        
        if (status.IsLoading)
        {
            response.AppendLine("üîÑ **Loading Teams App Catalog Data...**");
            response.AppendLine($"üìä Apps loaded: {status.AppDefinitionsLoaded:N0}");
            response.AppendLine($"üîê Entitlements loaded: {status.EntitlementsLoaded:N0}");
            if (status.CacheEfficiency > 0)
            {
                response.AppendLine($"‚ö° Cache efficiency: {status.CacheEfficiency:F1}%");
            }
        }
        else if (status.IsComplete)
        {
            response.AppendLine("‚úÖ **Catalog Data Loaded Successfully**");
            response.AppendLine();
            response.AppendLine($"üìä **Statistics:**");
            response.AppendLine($"‚Ä¢ Apps loaded: {status.AppDefinitionsLoaded:N0}");
            response.AppendLine($"‚Ä¢ Entitlements processed: {status.EntitlementsLoaded:N0}");
            response.AppendLine($"‚Ä¢ Load time: {status.LoadDuration?.TotalSeconds:F1} seconds");
            response.AppendLine($"‚Ä¢ Cache efficiency: {status.CacheEfficiency:F1}%");
            
            if (status.LastLoadTime.HasValue)
            {
                response.AppendLine($"‚Ä¢ Last updated: {status.LastLoadTime:yyyy-MM-dd HH:mm:ss} UTC");
            }
            
            response.AppendLine();
            response.AppendLine("üîç **Ready to search!** Try asking me:");
            response.AppendLine("‚Ä¢ \"Find Microsoft apps\"");
            response.AppendLine("‚Ä¢ \"Show apps available in R1\"");
            response.AppendLine("‚Ä¢ \"What apps are pre-consented?\"");
        }
        else
        {
            response.AppendLine("‚ö†Ô∏è **Catalog data not loaded**");
            if (status.Errors.Any())
            {
                response.AppendLine("**Errors encountered:**");
                foreach (var error in status.Errors.Take(3))
                {
                    response.AppendLine($"‚Ä¢ {error}");
                }
            }
        }

        return response.ToString();
    }

